export interface TemplateCategory {
  id: string;
  name: string;
  templates: Template[];
}

export interface Template {
  id: string;
  title: string;
  content: string;
}

// Parse templates from text files
function parseTemplates(text: string, categoryName: string): Template[] {
  const templates: Template[] = [];
  const lines = text.split('\n');
  let currentTemplate: { title: string; content: string[] } | null = null;
  let templateIndex = 0;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    
    // Skip empty lines at the start
    if (!line && !currentTemplate) continue;
    
    // Skip category title line
    if (line === categoryName || line === '═══════════════════════════════════════') {
      continue;
    }

    // Check if line is a separator (multiple dashes or equals)
    const isSeparator = /^[-=]+$/.test(line);
    
    if (isSeparator) {
      // Save current template if exists
      if (currentTemplate && currentTemplate.content.length > 0) {
        templates.push({
          id: `${categoryName}-${templateIndex++}`,
          title: currentTemplate.title || 'ไม่มีหัวข้อ',
          content: currentTemplate.content.join('\n').trim(),
        });
        currentTemplate = null;
      }
      continue;
    }

    // Check if line looks like a title (short line, not too long, followed by separator or empty line)
    if (!currentTemplate && line && line.length < 100) {
      // Check if next non-empty line is separator or content
      let nextNonEmpty = '';
      for (let j = i + 1; j < lines.length; j++) {
        const nextLine = lines[j].trim();
        if (nextLine) {
          nextNonEmpty = nextLine;
          break;
        }
      }
      
      if (!nextNonEmpty || /^[-=]+$/.test(nextNonEmpty)) {
        currentTemplate = { title: line, content: [] };
        continue;
      }
    }

    // Add to current template content
    if (currentTemplate) {
      currentTemplate.content.push(line);
    } else if (line) {
      // Start new template without explicit title
      currentTemplate = { title: line.substring(0, 50), content: [line] };
    }
  }

  // Save last template
  if (currentTemplate && currentTemplate.content.length > 0) {
    templates.push({
      id: `${categoryName}-${templateIndex++}`,
      title: currentTemplate.title || 'ไม่มีหัวข้อ',
      content: currentTemplate.content.join('\n').trim(),
    });
  }

  return templates;
}

